export function chatJsonFromSchema(schema: Record<string, unknown>): unknown {
  const required = (schema.required as string[] | undefined) ?? [];
  if (required.includes("preparedContext")) return { preparedContext: "Prepared story." };
  if (required.includes("summary") && required.includes("changeTypes")) {
    return modelLayerPayload();
  }
  if (required.includes("summary") && required.length === 1) {
    return { summary: "Final commit summary." };
  }
  if (
    required.includes("signalReasons") &&
    !required.includes("changeTypes") &&
    (schema.properties as Record<string, Record<string, unknown>>)?.signalReasons?.type ===
      "object"
  ) {
    return {
      signalReasons: {
        technical: "Polished technical reason.",
        architecture: "",
        security: "",
      },
    };
  }
  if (required.includes("questionsAnalysis") && !required.includes("summary")) {
    return {
      questionsAnalysis: [
        {
          observation: "Shows feature work.",
          missingPiece: "Production scope unknown.",
          question: "Did this ship to production?",
        },
        {
          observation: "Touches auth module.",
          missingPiece: "Ownership unclear.",
          question: "Did you design the auth flow?",
        },
        {
          observation: "Adds tests.",
          missingPiece: "Coverage intent unknown.",
          question: "Were these tests required by review?",
        },
        {
          observation: "Config changes present.",
          missingPiece: "Environment unknown.",
          question: "Which environment does this target?",
        },
      ],
    };
  }
  if (required.includes("summary") && required.includes("domains")) {
    return {
      summary: "CLI",
      domains: ["tooling"],
      apparentStack: ["TypeScript"],
      keyThemes: ["git"],
    };
  }
  if (required.includes("routine")) return { routine: false, reason: "feature work" };
  if (required.includes("needed")) return { needed: true, text: "Added new capability." };
  const historyProp = (schema.properties as Record<string, Record<string, unknown>> | undefined)
    ?.history;
  if (required.includes("history") && historyProp?.type === "array") {
    return { history: ["Condensed history entry."] };
  }
  if (required.includes("history") && historyProp?.type === "string") {
    return { history: "Session history narrative." };
  }
  if (required.includes("hiContext")) return groupClassifyPayload();
  if (
    required.includes("signalReasons") &&
    (schema.properties as Record<string, Record<string, unknown>>)?.signalReasons?.type ===
      "array"
  ) {
    return { signalReasons: ["r1", "r2", "r3", "r4"] };
  }
  if (required.includes("evidenceExcerpts")) {
    const props = (schema.properties as Record<string, Record<string, unknown>> | undefined)
      ?.evidenceExcerpts;
    const count = typeof props?.maxItems === "number" ? props.maxItems : 4;
    return {
      evidenceExcerpts: Array.from(
        { length: count },
        (_, i) => `- Scoped evidence ${i + 1}.\nRelated commits: abc12345`,
      ),
    };
  }
  if (required.includes("lineage")) {
    return {
      lineage: [
        {
          derivedFromThreadIds: ["1700000100000000"],
          derivedFromReportSignalRefs: [{ dimension: "technical", index: 0 }],
          derivedFromPreparedSignalSlots: [0],
        },
      ],
    };
  }
  if (required.includes("questionsAnalyses") && required.includes("confidence")) {
    return {
      questionsAnalyses: [
        { observation: ["o1"], missingPiece: ["m1"], question: ["q1"] },
        { observation: ["o2"], missingPiece: ["m2"], question: ["q2"] },
        { observation: ["o3"], missingPiece: ["m3"], question: ["q3"] },
        { observation: ["o4"], missingPiece: ["m4"], question: ["q4"] },
      ],
      confidence: "medium",
    };
  }
  if (required.includes("technologies") && !required.includes("summary")) {
    return { technologies: ["TypeScript"] };
  }
  if (required.includes("changeTypes") && required.includes("hiContext")) {
    return {
      changeTypes: ["feature"],
      signalReasons: { technical: ["t"], architecture: [], security: [] },
      questionsAnalyses: [],
      confidence: "medium",
      hiContext: ["Core"],
      mediumContext: [],
      lowContext: [],
    };
  }
  if (required.includes("narrative")) {
    return { narrative: ["Bullet one", "Bullet two", "Bullet three", "Bullet four"] };
  }
  if (required.includes("cvBullets")) {
    return {
      cvBullets: [
        "Built CLI tooling for git history reconstruction.",
        "Designed LLM pipeline stages for commit summarization.",
        "Implemented token usage tracking across pipeline steps.",
        "Refactored report merge for append-only resume semantics.",
      ],
    };
  }
  return modelLayerPayload();
}

