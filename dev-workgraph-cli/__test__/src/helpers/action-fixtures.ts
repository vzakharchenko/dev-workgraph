export function seedCommit(
  repoPath: string,
  overrides: Partial<CommitRecord> & { commitHash: string },
  period?: string,
): { jsonPath: string; patchPath: string; summaryPath?: string } {
  const { model, ...rest } = overrides;
  const record = sampleCommit({ ...rest, model: null });
  const dir = path.join(repoCommitsDir(repoPath, period), String(record.timestamp));
  fs.mkdirSync(dir, { recursive: true });
  const jsonPath = path.join(dir, `${record.commitHash}.json`);
  const patchPath = path.join(dir, `${record.commitHash}.patch`);
  const evidence: CommitEvidenceRecord = {
    commitHash: record.commitHash,
    timestamp: record.timestamp,
    title: record.title,
    author: record.author,
    deterministic: record.deterministic,
  };
  fs.writeFileSync(jsonPath, `${JSON.stringify(evidence, null, 2)}\n`);
  fs.writeFileSync(patchPath, "diff --git a/src/a.ts\n+export const x = 1;\n");
  if (model) {
    const summaryPath = seedSummary(repoPath, record, model, period);
    return { jsonPath, patchPath, summaryPath };
  }
  return { jsonPath, patchPath };
}

/** Seeds a split evidence export (manifest + two parts). */
export function seedSplitCommit(
  repoPath: string,
  commitHash: string,
  period?: string,
): { manifestPath: string; partPaths: string[] } {
  const timestamp = 1_700_000_000;
  const dir = path.join(repoCommitsDir(repoPath, period), String(timestamp));
  fs.mkdirSync(dir, { recursive: true });

  const manifest: CommitEvidenceRecord = {
    commitHash,
    timestamp,
    title: "Large change",
    author: "dev@example.com",
    split: true,
    partCount: 2,
    deterministic: emptyDeterministic(),
  };
  const manifestPath = path.join(dir, `${commitHash}.json`);
  fs.writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);

  const partPaths: string[] = [];
  for (const part of [1, 2]) {
    const partJson = path.join(dir, `${commitHash}.part${part}.json`);
    const partPatch = path.join(dir, `${commitHash}.part${part}.patch`);
    fs.writeFileSync(
      partJson,
      `${JSON.stringify(
        {
          commitHash,
          timestamp,
          title: manifest.title,
          author: manifest.author,
          part,
          partCount: 2,
          patchTruncated: false,
          deterministic: emptyDeterministic(),
        },
        null,
        2,
      )}\n`,
    );
    fs.writeFileSync(
      partPatch,
      `diff --git a/src/part${part}.ts b/src/part${part}.ts\n+export const p${part} = ${part};\n`,
    );
    partPaths.push(partJson);
  }

  return { manifestPath, partPaths };
}

/** Seeds a split export with a binary/autogenerated bucket as the last part. */
export function seedSplitCommitWithBucket(
  repoPath: string,
  commitHash: string,
  period?: string,
): { manifestPath: string; partPaths: string[] } {
  const timestamp = 1_700_000_000;
  const dir = path.join(repoCommitsDir(repoPath, period), String(timestamp));
  fs.mkdirSync(dir, { recursive: true });

  const manifest: CommitEvidenceRecord = {
    commitHash,
    timestamp,
    title: "Large change",
    author: "dev@example.com",
    split: true,
    partCount: 3,
    pathFilterApplied: true,
    deterministic: emptyDeterministic(),
  };
  const manifestPath = path.join(dir, `${commitHash}.json`);
  fs.writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);

  const partPaths: string[] = [];
  for (const part of [1, 2]) {
    const partJson = path.join(dir, `${commitHash}.part${part}.json`);
    const partPatch = path.join(dir, `${commitHash}.part${part}.patch`);
    fs.writeFileSync(
      partJson,
      `${JSON.stringify(
        {
          commitHash,
          timestamp,
          title: manifest.title,
          author: manifest.author,
          part,
          partCount: 3,
          patchTruncated: false,
          deterministic: emptyDeterministic(),
        },
        null,
        2,
      )}\n`,
    );
    fs.writeFileSync(
      partPatch,
      `diff --git a/src/part${part}.ts b/src/part${part}.ts\n+export const p${part} = ${part};\n`,
    );
    partPaths.push(partJson);
  }

  const bucketJson = path.join(dir, `${commitHash}.part3.json`);
  const bucketPatch = path.join(dir, `${commitHash}.part3.patch`);
  fs.writeFileSync(
    bucketJson,
    `${JSON.stringify(
      {
        commitHash,
        timestamp,
        title: manifest.title,
        author: manifest.author,
        part: 3,
        partCount: 3,
        patchTruncated: false,
        isBinaryOrAutogenerated: true,
        deterministic: {
          ...emptyDeterministic(),
          changedFiles: {
            added: ["build/out.bin"],
            modified: [],
            deleted: [],
            renamed: [],
          },
        },
      },
      null,
      2,
    )}\n`,
  );
  fs.writeFileSync(bucketPatch, "commit abc\n\n# build/out.bin\n");
  partPaths.push(bucketJson);

  return { manifestPath, partPaths };
}

export function seedSummary(
  repoPath: string,
  commit: Pick<CommitRecord, "commitHash" | "timestamp">,
  model: NonNullable<CommitRecord["model"]>,
  period?: string,
): string {
  const summary: CommitSummaryRecord = {
    commitHash: commit.commitHash,
    timestamp: commit.timestamp,
    sourceEvidence: String(commit.timestamp),
    model,
  };
  const summaryPath = path.join(
    repoSummariesDir(repoPath, period),
    String(commit.timestamp),
    `${commit.commitHash}.json`,
  );
  fs.mkdirSync(path.dirname(summaryPath), { recursive: true });
  fs.writeFileSync(summaryPath, `${JSON.stringify(summary, null, 2)}\n`);
  return summaryPath;
}

